$(".nav-link.item.one").on("mouseover", function () {
    $(".nav-link.item.one").css("color", "white");
    $(".nav-link.item.one").animate({
                fontSize: "1.55pc"
            });
});

$(".nav-link.item.one").on("mouseleave", function () {
    $(".nav-link.item.one").css("color", "blanchedalmond");
    $(".nav-link.item.one").animate({
                fontSize: "1.4pc"
    });
});
 
